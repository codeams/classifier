
def translate_label(label):
    if label == 'coconut':
        return 'coco'
    elif label == 'banana':
        return 'platano'
    elif label == 'apple':
        return 'manzana roja'
    elif label == 'blueberry':
        return 'mora azul'
    elif label == 'avocado':
        return 'aguacate'
    else:
        return 'Desconocido'
